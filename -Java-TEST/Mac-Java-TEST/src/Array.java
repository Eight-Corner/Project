/*
 * 배열(array)은 같은 타입의 변수들로 이루어진 유한 집합으로 정의된다.
 * 배열을 구성하는 각각의 값을 배열 요소(element)라고 하며,
 * 배열에서의 위치를 가리키는 숫자는 인덱스(index)라고 한다.
 */
public class Array {

	public static void main(String[] args) {
		String[] lol = {"Darius", "Garen", "Zark", "Oriana", "Atrox"};
	    // 인덱스 번호 :   0  ,    1   ,   2   ,     3      ,     4
	System.out.println(lol[0]); // Darius
	System.out.println(lol[1]); // Garen
	System.out.println(lol[2]); // Zark
	System.out.println(lol[3]); // Oriana
	System.out.println(lol[4]); // Atrox
	

	}

}
